#include "libft.h"

int	ft_isnum(char **str, int i)
{
	int	j;

	j = 0;
	while (str[i])
	{
		j = 0;
		while (str[i][j] == ' ')
			j++;
		if (str[i][j] == '-' || str[i][j] == '+')
		{
			if (!(str[i][j + 1] >= '0' && str[i][j + 1] <= '9'))
				return (-1);
			j++;
		}
		while (str[i][j] != '\0')
		{
			if (!(str[i][j] >= '0' && str[i][j] <= '9'))
				return (-1);
			j++;
		}
		i++;
	}
	return (0);
}
